public class Coordinates
{
    private float lon;
    private float lat;

    public float getLon()
    {
        return lon;
    }

    public float getLat()
    {
        return lat;
    }

    public void setLon(float lon_)
    {
        lon = lon;
    }

    public void setLat(float lat_)
    {
        lat = lat_;
    }
}
